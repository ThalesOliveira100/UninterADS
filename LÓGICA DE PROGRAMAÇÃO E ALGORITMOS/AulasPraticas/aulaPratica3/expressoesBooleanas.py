# Exercício de escrita de expressoes booleanas em python
# O somatório de 2 com 2 é menor do que 4

print(2 + 2 < 4)

# O valor7 // 3 é igual a 1 + 1

print(7 // 3 == 1 + 1)

# A soma de 3 elevado ao quadrado com 4 elevado ao quadrado é igual a 25

print(3 ^ 2 + 4 ^ 2 == 25)

# A soma de 2, 4 e 6 é maior do que 12
print(2 + 4 + 6 > 12)

# 1387 é divisível por 19

print(1387 % 19 == 0)

# 31 é par

print(31 % 2 == 0)

# O menor valor entre: 34, 29 e 31 é menor do que 30
print(min(34, 29, 31) < 30)
